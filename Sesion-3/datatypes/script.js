//Primitive data types

var number = 23 //number
var name1 = "Raghu" //string
var condition = false //boolean
var data = null //null

console.log("number "+number)
console.log("string "+name1)
console.log("boolean "+condition)
console.log("null "+data)

console.log(number,typeof(number))
console.log(name1,typeof(name1))
console.log(condition,typeof(condition))
console.log(data,typeof(data))

var cars = ["BMW","Merc","volvo"]; //Array
var user = {name:'Raghu',age:22,Town:"Malavalli"} //object
console.log(cars, typeof cars)
console.log(user, typeof user)
console.log("2nd element of cars is "+ cars[2])
console.log("user Town "+ user.Town)

